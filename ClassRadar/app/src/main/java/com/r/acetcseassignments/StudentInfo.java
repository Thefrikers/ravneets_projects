package com.r.acetcseassignments;

public class StudentInfo {

    public String rollNumber;
    public String email;
    public String name;
    public String batch;
    public String type;
    public String imgUrl;
    public String accountType;

    public StudentInfo(String rollNumber, String email, String name, String batch, String type, String imgUrl, String accountType) {
        this.rollNumber = rollNumber;
        this.email = email;
        this.name = name;
        this.batch = batch;
        this.type = type;
        this.imgUrl = imgUrl;
        this.accountType = accountType;
    }
}
